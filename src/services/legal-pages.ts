export function getLegalDocuments(): LegalDocument[] {
  return legalDocuments.map((document) => ({ ...document }));
}

export function findLegalDocument(pathname: string): LegalDocument | undefined {
  return legalDocuments.find((document) => document.path === pathname);
}

export function renderLegalDocumentHtml(pathname: string, baseUrl: string): string | undefined {
  const document = findLegalDocument(pathname);
  if (!document) return undefined;
  return renderLegalShell(document, baseUrl);
}
